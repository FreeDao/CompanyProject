package com.zgan.community.data;

public class Weather {
    private String desc;
    private String Wd;
    private String Sd;
    private String Zwx;
    private String Kq;
    private String tq;
    private String Fl;
    private String icon;
    
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getWd() {
		return Wd;
	}
	public void setWd(String wd) {
		Wd = wd;
	}
	public String getSd() {
		return Sd;
	}
	public void setSd(String sd) {
		Sd = sd;
	}
	public String getZwx() {
		return Zwx;
	}
	public void setZwx(String zwx) {
		Zwx = zwx;
	}
	public String getKq() {
		return Kq;
	}
	public void setKq(String kq) {
		Kq = kq;
	}
	public String getTq() {
		return tq;
	}
	public void setTq(String tq) {
		this.tq = tq;
	}
	public String getFl() {
		return Fl;
	}
	public void setFl(String fl) {
		Fl = fl;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
}
